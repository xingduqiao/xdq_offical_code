				<h3 class="nd-title">“青城•未来院子”前期物业服务招标公告</h3>
				<p class="nd-desc"></p>
				<div class="nd-time-share clearfix">
					<div class="nts-time"><span>来源：<a href="/wyxxp/">物业选续聘</a></span>&nbsp;&nbsp;&nbsp;<span> 发布时间：2021-11-05 14:06:35</span></div>
					
				</div>
				<div class="nd-paragraph pageC">
<p>按照国务院《物业管理条例》、《成都市物业管理条例》等法规、政策的规定，成都建工集团旅游有限公司决定对青城&middot;未来院子项目物业服务采用公开招标的方式选聘物业服务企业，现将有关事项公告如下：</p>
<p><b>一、招标建筑区划的简要说明</b></p>
<p>项目名称：青城&middot;未来院子</p>
<p>地点：都江堰市青城山镇沿江村4、5、7组，芒城村2、4组</p>
<p>四至范围：</p>
<p>南至：聚青路 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 北至：规划道路</p>
<p>东至：规划道路 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 西至：规划道路</p>
<p>物业类型为：住宅、商业&nbsp;&nbsp; &nbsp; &nbsp; &nbsp; 总建筑面积186152.39平方米</p>
<p>收费面积：114850.42平方米（最终收费面积以实际交付面积为准）</p>
<p>容积率：&nbsp;&nbsp;0.88 &nbsp; &nbsp;&nbsp;&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;绿地率：&nbsp;28.6% &nbsp;</p>
<p>机动车位：建筑区划内按规划配备的机动车停放库（位）1076个，非机动车库1559.42平方米。销售前所有权属开发建设单位所有，由开发建设单位承担管理维护责任；销售后所有权属已购单个业主所有，由已购单个业主承担管理维护责任。 &nbsp; &nbsp; &nbsp;</p>
<p><b>二、物业服务用房的配置情况</b></p>
<p>物业服务用房位于42#楼1F、2F，建筑面积：328.62平方米，10#楼1F，建筑面积：136.09平方米（含30平方米业委会议事用房）。（房屋建成后按照《成都市物业管理条例》相关要求在物业服务用房地上部分设置）</p>
<p><b>三、招标人</b></p>
<p>本项目招标人：成都建工集团旅游有限公司</p>
<p>联系地址：四川省成都市都江堰市青城山镇豪生路33号</p>
<p><b>四、</b><b>资格预审</b></p>
<p>本次招标为公开招标，可设置资格预审程序，凡具备本招标项目的能力并且具备规定的资格条件的物业企业，均可向招标人报名。在投标报名时间截止之时，如报名单位少于5家，则取消资格预审程序，报名单位可直接于2021年10月19日至2021年10月20日早上9：00-12：00，下午14：00-17：00内，在成都市锦江区宏济新路5号SOHO商务港207室购买招标文件；如报名单位多于5家（含5家），则设置资格预审程序，报名单位可直接于2021年10月19日早上9：00&mdash;12：00，下午14：00&mdash;17：00内在成都市锦江区宏济新路5号SOHO商务港207室购买资格预审文件，<b>只有收到资格预审合格书的投标申请人才能领取招标文件，参加投标。</b></p>
<p><b>五、投标单位资格要求</b></p>
<p>前期物业服务主体为依法设立、从事物业服务活动、具有独立法人资格、具有专业物业管理能力且符合：</p>
<p>&nbsp;</p>
<p>1、在中华人民共和国境内注册，具有独立法人资格的合法企业；</p>
<p>&nbsp;</p>
<p>2、在成都房地产行业信用信息平台中上年度信用记分高于100分（含）或在住建蓉e办-智慧物业政务服务平台中企业信用记录B级（含）及以上；</p>
<p>&nbsp;</p>
<p>3、未被列入信用预警、警示对象。</p>
<p><b>六、报名时间及地点</b></p>
<p>1、凡符合上面第五条要求的物业公司，有资格参与本次投标，请于2021 年10月15日、2021年10月18日（上午9:00&mdash;&mdash;12:00，下午14:00&mdash;&mdash;17:00）,前往成都市锦江区宏济新路5号SOHO商务港2楼207号报名。</p>
<p>2、报名时企业经办人员需携带单位介绍信、经办人身份证复印件、营业执照复印件等并盖鲜章、本单位社保，在成都房地产行业信用信息平台中2020年年度信用记分不低于100分或住建蓉e办-智慧物业政务服务平台中企业信用记录B级（含）及以上网页截图。（复印件加盖鲜章，原件备查）。</p>
<p>3.资格预审文件每套售价为人民币捌佰元整，招标文件每套售价为人民币捌佰元整（现金，招标文件售后不退,投标资格不能转让。不提供邮购招标文件服务。只有购买了招标文件，才能参与投标）</p>
<p><b>七、投标文件的递交</b></p>
<p>2021年&nbsp;&nbsp;11 &nbsp;&nbsp;月&nbsp;16&nbsp;日下午&nbsp;14&nbsp;点&nbsp;00&nbsp;分（北京时间），投标人应于递交投标文件截止时间之前将投标文件送达评审地点，逾期送达的将被拒绝。</p>
