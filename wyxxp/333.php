				<h3 class="nd-title">“聚亿玺悦府”前期物业服务招标公告</h3>
				<p class="nd-desc"></p>
				<div class="nd-time-share clearfix">
					<div class="nts-time"><span>来源：<a href="/wyxxp/">物业选续聘</a></span>&nbsp;&nbsp;&nbsp;<span> 发布时间：2021-11-05 14:05:03</span></div>
					
				</div>
				<div class="nd-paragraph pageC">
<p>按照国务院《物业管理条例》、《成都市物业管理条例》等法规、政策的规定，四川省成都市聚亿房地产开发有限公司决定对&ldquo;聚亿玺悦府&rdquo;的前期物业服务采用公开招标的方式选聘物业服务企业，现将有关事项公告如下：</p>
<p>一、招标建筑区划的简要说明</p>
<p>项目名称：聚亿玺悦府</p>
<p>建设单位：四川省成都市聚亿房地产开发有限公司</p>
<p>本建筑区划位于成都市武侯区武兴四路1131号，四至范围：东至武兴四路，西至规划用地，南至智远巷，北至武青西三路（见附图）。</p>
<p>本建筑区划规划设计的物业类型为：住宅。</p>
<p>本建筑区划规划建设净用地面积21477.53平方米。</p>
<p>本建筑区划总建筑面积68492.26平方米。地上计入容积率的建筑面积&nbsp;42955.00平方米，其中住宅建筑面积42817.50平方米，非住宅建筑面积137.50平方米（项目配套设施137.50平方米）；地上不计入容积率的建筑面积872.26平方米；地下建筑面积24665.00平方米（含地下住宅（储藏室）4549.42平方米）。</p>
<p>本建筑区划共计建筑物7幢（其中住宅6幢 376户）；建筑结构为框架结构。</p>
<p>本建筑区划容积率为2.00，绿地率为35.00%。</p>
<p>本建筑区划规划设计建造地下机动车停车位573个，地下非机动车停车位477个。</p>
<p>二、物业服务用房的配置情况</p>
<p>1、物业服务企业或者其他管理人用房：</p>
<p>建筑面积：237.13&nbsp;平方米（含业主委员会活动室）</p>
<p>坐落位置：4号楼1层、-1层</p>
<p>2、业主委员会议事活动用房：</p>
<p>建筑面积为37.37平方米；</p>
<p>坐落位置：4号楼1层。</p>
<p>三、招标人</p>
<p>本项目招标人：四川省成都市聚亿房地产开发有限公司</p>
<p>联系地址：成都市武侯区武兴三路603号附651号</p>
<p>联系人：冯先生</p>
<p>四、资格预审</p>
<p>本次招标为公开招标，可设置资格预审程序，凡具备管理本招标项目的能力并且具备规定的资格条件的物业管理企业，均可向招标人报名。在投标报名时间截止之时，如报名单位少于5家（含5家），则取消资格预审程序，报名单位可直接于2021年10月20日、2021年10月21日工作时间内在成都市龙泉驿区翠龙街66号第九空间大厦22楼2216号获取招标文件；如报名单位多于5家，则设置资格预审程序，报名单位可直接于2021年10月20日工作时间内在成都市龙泉驿区翠龙街66号第九空间大厦22楼2216号购买资格预审文件，只有资格预审合格的投标申请人才能参加投标。</p>
<p>五、投标单位资格要求</p>
<p>前期物业服务主体为依法设立、从事物业服务活动、具有独立法人资格、具有专业物业管理能力且符合：</p>
<p>1、在中华人民共和国境内注册，具有独立法人资格的合法企业；</p>
<p>2、为建筑区划配备的物业服务力量与本市物业服务力量配置指导标准相对应；</p>
<p>3、在成都房地产行业信用信息平台中上年度信用记分高于100分（含）或在住建蓉e办-智慧物业政务服务平台中企业信用记录B级（含）及以上；</p>
<p>4、未被列入信用预警、警示对象。</p>
<p>六、投标报名时间</p>
<p>1．决定报名的投标单位于2021年10月19日17时00分前在成都市龙泉驿区翠龙街66号第九空间大厦22楼2216号报名（电话：84877566）。</p>
<p>2．在报名时应提供经办人员单位介绍信或法定代表人委托书原件、营业执照副本、经办人身份证原件和复印件1份（验原件收复印件，所有复印件加盖公章）、成都房地产行业信用信息平台企业信用记分或住建蓉e办-智慧物业政务服务平台中企业信用记录截图1份（加盖公章）。</p>
<p>3．资格预审文件每套售价为人民币叁佰元，招标文件每套售价为人民币陆佰元售后不退。</p>
<p>七、招标人将对资格预审合格的投标申请人发送《资格预审合格通知书》，凡资格预审合格的投标申请人，均可按照通知书中确定的时间、地方和方式获取招标文件。</p>
