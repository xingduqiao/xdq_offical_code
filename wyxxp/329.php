				<div class="nd-paragraph pageC">
<p>按照国务院《物业管理条例》、《成都市物业管理条例》等法规、政策的规定，成都远实房地产开发有限责任公司决定对&ldquo;森海境府&rdquo;的物业服务采用公开招标的方式选聘物业服务企业，现将有关事项公告如下：</p>
<p><b>一、</b><b>招标建筑区划的简要说明</b></p>
<p>项目名称：森海境府</p>
<p>建设单位：成都远实房地产开发有限责任公司</p>
<p>本建筑区划位于：成都市新都区新都街道桂林社区4、5组，万和社区8组</p>
<p>东至：绕城大道南一段； 南至：凌波东路； 西至：规划道路；北至：卫康路</p>
<p>本建筑区划规划设计的物业类型为：住宅。</p>
<p>&ldquo;森海境府&rdquo;规划总建筑面积226818.25平方米。 &nbsp;其中住宅建筑面积143977.50平方米； 非住宅建筑面积494.50平方米、 物业管理用房786.18平方米（含业委会用房30平方米）。</p>
<p>本建筑区划基地面积：11177.77㎡； &nbsp;　绿地面积：20226.5㎡</p>
<p>本建筑区划规划建设机动车停车位）1887辆（其中住宅机动车停车位1879辆，配套机动车停车位8辆）。</p>
<p><b>二、物业服务用房的配置情况</b></p>
<p>物业管理用房建筑总面积为：786.18平方米（含业委会用房30平方米），位于：3#楼1F/2F、30#楼1F/2F、2#楼-1F、22#楼-1F。</p>
<p>业委会用房：30平方米；位于：3#楼1F/2F。</p>
<p><b>三、招标人</b></p>
<p>本项目招标人：成都远实房地产开发有限责任公司</p>
<p>联系地址：四川省成都市新都区斑竹园街道中利路169号2栋10层7号</p>
<p>联系电话： 13908066982 &nbsp; &nbsp; &nbsp;联系人：李先生</p>
<p><b>四、投标单位资格要求</b></p>
<p>（一）前期物业服务的主体为依法设立、从事物业服务活动、具有独立法人，具有专业物业管理能力。</p>
<p>(二)建立了企业及从业人员的信用信息，且在成都房地产行业信用信息平台中2021年年度信用记分不低于100分或住建蓉e办-智慧物业政务服务平台中企业信用记录B级（含）及以上；</p>
<p>（三）未被成都房地产行业信用信息平台中列入信用预警、警示对象。</p>
<p><b>五、投标报名时间</b></p>
<p>1．有意向的投标人请于2021年10月29日到11月2日工作日上午9:30--12:00；下午:14:30--17:00到成都市锦江区宏济新路5号报名，报名预约电话：13908066982 。</p>
<p>2．决定报名的投标单位在报名时应提供经办人员单位介绍信或法定代表人委托书原件、营业执照副本复印件、成都房地产行业信用信息平台企业信用记分截图一份复印件，经办人身份证原件和复印件(所有复印件须加盖投标单位鲜章)。</p>
<p><b>六、招标文件出售时间<a name="_GoBack"></a></b></p>
<p>1. &nbsp;报名单位可于2021年11月3日至2021年11月4日工作时间9:00-12:00、13:00-17:00内，前往成都市锦江区宏济新路5号购买招标文件。</p>
<p>2.招标文件每套售价为人民币捌佰元整(中标与否均不退)。</p>
