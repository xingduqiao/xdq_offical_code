				<h3 class="nd-title">“佳兆业印月花园”前期物业服务招标公告</h3>
				<p class="nd-desc"></p>
				<div class="nd-time-share clearfix">
					<div class="nts-time"><span>来源：<a href="/wyxxp/">物业选续聘</a></span>&nbsp;&nbsp;&nbsp;<span> 发布时间：2021-11-05 14:05:44</span></div>
					
				</div>
				<div class="nd-paragraph pageC">
<p>按照国务院《物业管理条例》 和《成都市物业管理招标投标暂行办法》等法规、政策的规定，成都佳澎房地产开发有限公司现决定对&ldquo;佳兆业印月花园&rdquo;前期物业服务公开招标。</p>
<p><b>一、招标建筑区划的简要说明</b></p>
<p>本项目位于成都市天府新区华阳街道二江寺村六、十一、鹤林村二组。物业类型为：住宅、商业。规划建设净用地面积50538.19平方米，总建筑面积165467.46平方米。</p>
<p>（一）地上计容建筑面积98066.14平方米。其中，</p>
<p>1、住宅建筑面积97767.48平方米。</p>
<p>2、非住宅建筑面积298.66平方米。</p>
<p>（1）建设项目配套设施建筑面积298.66平方米。</p>
<p>A、物管用房（含业主委员会活动室30㎡）建筑面积198.66平方米；</p>
<p>B、市政设施用房建筑面积50平方米。</p>
<p>（二）地上不计容建筑面积2180.84平方米。其中，</p>
<p>1、架空层建筑面积579.79平方米；</p>
<p>&nbsp; &nbsp;2、出地面风井、楼梯电梯建筑面积120.37平方米；</p>
<p>3、保温层建筑面积1480.68平方米。</p>
<p>（三）地下（含半地下）建筑面积65220.48平方米。其中，其中，半地下商业设施3033.86平方米，地下配套设施62186.62平方米。</p>
<p>（1）地下机动车库面积43307.60平方米；</p>
<p>（2）地下非机动车库面积920.00平方米；</p>
<p>（3）地下垃圾用房面积200.00平方米；</p>
<p>（4）地下机动车清洗站150.00平方米；</p>
<p>（5）地下物管用房面积248.66平方米；</p>
<p>（6）消防控制室建筑面积57.00平方米；</p>
<p>（7）市政设施用房面积50.00平方米；</p>
<p>（8）地下住宅储藏间面积14654.27平方米；</p>
<p>&nbsp; &nbsp;（9）设备用房2599.09平方米。</p>
<p>本项目容积率2.00；绿地率35.00%，总建筑密度20.00%。</p>
<p>全民健身场所建筑面积800.00平方米。</p>
<p><b>二、物业服务用房的配置情况</b></p>
<p>（一）物业服务企业位于：20号楼1F 248.66㎡，2号楼-1F 248.66㎡。</p>
<p>总建筑面积：497.32㎡（含30.00㎡业主委员会活动用房）</p>
<p><b>三、投标单位资格要求</b></p>
<p>1、为依法设立、从事物业服务活动、具有独立法人资格的物业服务企业；</p>
<p>&nbsp; &nbsp;2、在成都房地产行业信用信息平台中2020年度信用记分累计不低于100分或在住建蓉e办-智慧物业政务服务平台信用等级不低于B级。</p>
<p><b>&nbsp; &nbsp;</b><b>四、投标报名时间及报名资料</b></p>
<p>（一）有意向的投标人请于2021年10月13日和10月14日工作时间9:00-12:00、13:00-17:00内，到成都市成都市锦江区教场坝东五街8号报名。</p>
<p>（二）决定报名的投标单位在报名时应提供以下资料：</p>
<p>1、经办人员单位介绍信或法定代表人授权委托书原件；</p>
<p>2、经办人员身份证原件；（验原件，收加盖投标公司鲜章复印件）</p>
<p>3、营业执照正本、副本原件（营业执照经营范围须具备本项目相关服务范围且在有效期内，验原件，收加盖投标公司鲜章复印件）；</p>
<p>4、成都房地产行业信用信息平台中2020年度信用记分累计不低于100分或在住建蓉e办-智慧物业政务服务平台信用等级截图。（网上验证、收加盖投标公司鲜章复印件）</p>
<p><b>五、资格预审</b></p>
<p>1、本次招标为公开招标，可设置资格预审程序，凡具备管理本招标项目能力并且具备规定资格条件的物业管理服务企业，均可向招标人报名。在投标报名时间截止之时，如报名单位少于5家(含5家)，则取消资格预审程序，按照招标公告第六条中时间、地点直接购买招标文件。如报名单位多于5家，则设置资格预审程序，报名合格的投标单位按照招标方通知的要求、时间及地点购买资格预审文件。资格预审文件每套售价为人民币捌佰元整(中标与否均不退)。</p>
<p><b>六、购买招标文件时间、地点</b></p>
<p>1、报名单位可直接于2021年10月15日至2021年10月18日工作时间9:00-12:00、13:00-17:00内，前往成都市成都市锦江区教场坝东五街8号购买招标文件。</p>
<p>2、招标文件每套售价为人民币壹仟元整(中标与否均不退)。</p>				    </div>
