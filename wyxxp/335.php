				<h3 class="nd-title">“保利时代云玺花园”前期物业服务招标公告</h3>
				<p class="nd-desc"></p>
				<div class="nd-time-share clearfix">
					<div class="nts-time"><span>来源：<a href="/wyxxp/">物业选续聘</a></span>&nbsp;&nbsp;&nbsp;<span> 发布时间：2021-11-05 14:06:18</span></div>
					
				</div>
				<div class="nd-paragraph pageC">
<p>按照国务院《物业管理条例》、《成都市物业管理条例》等法规、政策的规定，四川府河华益置业有限公司决定对&ldquo;保利时代云玺花园&rdquo;的物业服务进行公开招标。兹邀请合格投标人以密封标书的方式前来投标。</p>
<p><b>&nbsp; &nbsp;</b><b>一、招标建筑区划的简要说明</b></p>
<p>&nbsp; &nbsp;项目名称：保利时代云玺花园 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>&nbsp; &nbsp;建设单位：四川府河华益置业有限公司</p>
<p>本建筑区划位于成都市金牛区二环北二段西北桥北街1号，东至府河大道，西至公园绿地，南至规划道路，北至规划道路。</p>
<p>本项目规划设计的物业类型为：住宅。 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>&ldquo;保利时代云玺花园&rdquo;规划建设净用地面积10812.85平方米，总建筑面积68858.96平方米。住宅建筑面积48497.68平方米，物管用房建筑面积140.14平方米（含业委会议事用房30平方米），垃圾用房建筑面积10平方米；门卫用房建筑面积10平方米；外保温层面积415.8平方米，架空层面积234.78平方米，非机动车车库建筑面积（架空层）737.48平方米；地下建筑面积18813.08平方米，其中地下机动车车库建筑面积17186.87平方米，地下设备用房建筑面积1457.30平方米，地下物管用房建筑面积137.89平方米，垃圾用房建筑面积31.02平方米；总绿地建筑面积3784.50平方米，全民健身场地面积300平方米，市政设施点位面积60平方米。</p>
<p>本项目容积率：4.5，总绿地率：35%。</p>
<p>&nbsp; &nbsp;建筑结构：框剪结构。</p>
<p>&nbsp;本项目规划建设机动车地下停车位488辆，地上非机动车停车位488辆。</p>
<p>二、物业<b>二、物业服务用房的配置情况</b></p>
<p>&nbsp; &nbsp;1、物业服务企业或者其他管理人用房：</p>
<p>建筑面积：278.03㎡，分别位于1#楼1F及夹层、1#楼-1F (地上：140.14㎡，地下137.89㎡)。<br />&nbsp; &nbsp;2、业主委员会议事活动用房：</p>
<p>建筑面积：30.00㎡，位于1#楼1F及夹层（物管用房内）。</p>
<p><b>三、招标人</b></p>
<p>&nbsp; &nbsp;本项目招标人：四川府河华益置业有限公司 &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>&nbsp; &nbsp;电 &nbsp; &nbsp;话：15280921285 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p>&nbsp;联 系 人：王艳妃 &nbsp; &nbsp;&nbsp;&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</p>
<p><b>四、资格预审</b></p>
<p>本次招标为公开招标，可设置资格预审程序，凡具备管理本招标项目的能力并且具备规定的资格条件的物业服务企业，均可向招标人报名。在投标报名时间截止之时，如合格的报名单位少于5家(含5家)，则取消资格预审程序，报名单位可直接于2021年10月19日至2021年10月20日工作时间内在成都市金牛区二环北二段西北桥北街1号项目所在地购买招标文件；如合格的报名单位多于5家，则设置资格预审程序,报名单位可直接于2021年10月19日工作时间内在成都市金牛区二环北二段西北桥北街1号项目所在地购买资格预审文件，只有收到资格预审合格书的投标申请人才能购买招标文件，参加投标。</p>
<p><b>五、投标单位资格要求</b></p>
<p>物业服务的主体为依法设立、从事物业服务活动、具有独立法人资格的物业服务企业，且符合：</p>
<p>(一)在中华人民共和国境内注册，具有独立法人资格的合法企业；</p>
<p>(二)为建筑区划配备的物业服务力量与本市物业服务力量配置指导标准相对应；</p>
<p>(三)在成都房地产行业信用信息平台中本年度信用记分高于100分（含）或住建蓉e办-智慧物业政务服务平台中企业信用记录B级（含）及以上；</p>
<p>（四）未被成都房地产行业信用信息平台中列入信用预警、警示对象。</p>
<p><b>六、投标报名时间</b></p>
<p>&nbsp; &nbsp;1．有意向的投标人请于2021年10月15日、2021年10月18日17:00前到成都市金牛区二环北二段西北桥北街1号项目所在地报名。</p>
<p>&nbsp; &nbsp;2．决定报名的投标单位在报名时应提供经办人员单位介绍信或法定代表人委托书原件、法人身份证复印件、营业执照正副本、经办人身份证及其他符合资格要求相关的证明材料。（所有复印件须加盖投标单位鲜章，验原件收复印件），经招标人审核通过后，方为合格的报名单位。</p>
<p>&nbsp; &nbsp;3．资格预审文件每套售价为人民币叁仟元整，招标文件每套售价为人民币叁仟元整(现金，中标与否均不退)。</p>
<p><b>七、招标人将对资格预审合格的投标申请人发送《资格预审合格通知书》，凡资格预审合格的投标申请人，均可按照通知书中确定的时间、地方和方式购买招标文件。</b></p>				    </div>
