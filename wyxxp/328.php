				<h3 class="nd-title">“中节能•世纪中心建设项目四期”前期物业服务招标公告</h3>
				<p class="nd-desc"></p>
				<div class="nd-time-share clearfix">
					<div class="nts-time"><span>来源：<a href="/wyxxp/">物业选续聘</a></span>&nbsp;&nbsp;&nbsp;<span> 发布时间：2021-11-04 21:56:43</span></div>
					
				</div>
				<div class="nd-paragraph pageC">
<p>按照《成都市物业管理条例》和《成都市物业管理招标投标暂行办法》的规定，成都中节能绿建投资有限公司决定对中节能&middot;世纪中心建设项目四期项目的物业服务采用公开招标的方式选聘物业服务企业，现将有关事项公告如下：<a name="_GoBack"></a></p>
<p><b>一、招标项目简要说明</b></p>
<p>1、本建筑区划位于成都市蒲江县健民路198号，四至范围：东至广场西路；西至前卫街；南至广平路；北至健民路。</p>
<p>2、本建筑区划规划设计的物业类型为：住宅、商业、办公、酒店</p>
<p>3、本建筑区划总建筑面积146621.29平方米。其中住宅建筑面积29195.51平方米（含公寓建筑面积4204.30平方米），商业建筑面积49072.69平方米，酒店建筑面积10887.03平方米，办公建筑面积19280.76平方米，门卫室建筑面积7.08平方米，物业管理用房621.46平方米（含业委会活动用房30平方米）；地上不计容建筑面积882.42平方米；地下机动车库28740平方米，地下非机动车库2848.53平方米，地下设备用房4965.01平方米，消防控制室120.80平方米。</p>
<p>容积率：2.48，绿地率：15.13%，建筑密度：47.71%</p>
<p>机动车位1091辆、非机动车位1572辆。</p>
<p><b>二、物业管理用房的配置情况</b></p>
<p>1、物业服务企业或者其他管理人用房：建筑面积621.46平方米位于健民路198号。</p>
<p>2、业主委员会议事活动用房：建筑面积30平方米位于健民路198号（物业管理用房内）。</p>
<p>三<b>、</b><b>招标人</b></p>
<p>本项目招标人：成都中节能绿建投资有限公司</p>
<p>联系电话：028-64055128</p>
<p>联系人：陈工</p>
<p><b>四、资格预审</b><br />本次招标为公开招标，可设置资格预审程序，凡具备管理本招标项目的能力并且具备规定的资格条件的物业管理企业，均可向招标人报名。在投标报名时间截止之时，如报名单位少于5家（含5家），则取消资格预审程序，报名单位可直接于2021年10月26日至2021年10月27日工作时间内在成都市高新区神仙树西路2号1栋3单元101室购买招标文件；如报名单位多于5家，则设置资格预审程序，报名单位可直接于2021年10月26日工作时间内在成都市高新区神仙树西路2号1栋3单元101室购买资格预审文件，凡资格预审合格的投标申请人，均可按照通知书中确定的时间、地点和方式获取招标文件。<br /><b>五、投标单位资格要求</b><br />物业服务的主体为依法设立、从事物业服务活动、具有独立法人，具有专业物业管理能力且符合：</p>
<p>（一）在中华人民共和国境内注册，具有独立法人资格的合法企业。</p>
<p>（二）为建筑区划配备的物业服务力量与本市物业服务力量配置指导标准相对应。</p>
<p>（三）在成都房地产行业信用信息平台中本年度信用记分高于100分（含）或在住建蓉e办-智慧物业政务服务平台中企业信用记录B级（含）及以上。</p>
<p>（四）未被成都房地产行业信用信息平台中列入信用预警、警示对象。</p>
<p><b>六、</b><b>投标报名时间</b></p>
<p>1．2021年10月22日至2021年10月25日17：00时前在成都市高新区神仙树西路2号1栋3单元101室报名。</p>
<p>2．企业有效营业执照正副本，组织机机构代码证正副本(三证合一的报名企业无需提供组织机构代码证)，法定代表人身份证，报名人身份证（以上证件证照查验原件，并留存加盖报名企业鲜章的复印件一份），成都房地产行业信用信息平台企业信用记分截图一份或住建蓉e办-智慧物业政务服务平台中企业信用记录截图一份（加盖报名企业公章），未被成都房地产行业信用信息平台中列入信用预警、警示对象承诺书原件一份，法定代表人委托书原件一份。<br />3.&nbsp;资格预审文件每套售价为人民币捌佰元整，招标文件每套售价为人民币捌佰元整，（现金，中标与否均不退）。</p>
